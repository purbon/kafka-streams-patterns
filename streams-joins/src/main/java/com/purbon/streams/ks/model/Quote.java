package com.purbon.streams.ks.model;

public class Quote {

    private long id;
    private String quote;

    public Quote() {

    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getQuote() {
        return quote;
    }

    public void setQuote(String quote) {
        this.quote = quote;
    }
}
